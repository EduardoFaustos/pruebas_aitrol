<center> <b>POLÍTICA DE PRIVACIDAD</b>   </center><br/>


La presente Política de Privacidad establece los términos en que usa y protege la información que
es proporcionada por sus usuarios al momento de utilizar nuestra App de IECED. Esta compañía está
comprometida con la seguridad de los datos de sus usuarios. Cuando le pedimos llenar los campos
de información personal con la cual usted pueda ser identificado, lo hacemos asegurando que sólo
se empleará de acuerdo con los términos de este documento. Sin embargo esta Política de
Privacidad puede cambiar con el tiempo o ser actualizada por lo que le recomendamos y
enfatizamos revisar continuamente esta página para asegurarse que está de acuerdo con dichos
cambios.
<br/>
<center><b>Información que es recogida</b></center> <br/>
App IECED podrá recoger información personal por ejemplo: Nombre, información de
contacto como su dirección de correo electrónica e información demográfica. Así mismo cuando
sea necesario podrá ser requerida información específica para procesar algún pedido o realizar una
entrega o facturación.
<br/>
 <center> <b>Uso de la información recogida</b>  </center> <br/>
App IECED emplea la información con el fin de proporcionar el mejor servicio posible,
particularmente para mantener un registro de usuarios, de pedidos en caso que aplique, y mejorar
nuestros productos y servicios. Es posible que sean enviados correos electrónicos periódicamente
a través de nuestro sitio con ofertas especiales, nuevos productos y otra información publicitaria
que consideremos relevante para usted o que pueda brindarle algún beneficio, estos correos
electrónicos serán enviados a la dirección que usted proporcione y podrán ser cancelados en
cualquier momento.
Está altamente comprometido para cumplir con el compromiso de mantener su información segura.
Usamos los sistemas más avanzados y los actualizamos constantemente para asegurarnos que no
exista ningún acceso no autorizado.
<br/>

<center> <b>Servicios de localización</b> </center><br/>

Para prestar servicios de localización la app de IECED podrá reunir, utilizar y compartir datos
exactos sobre ubicaciones, incluyendo la situación geográfica en tiempo real de su ordenador o de
su dispositivo bajo su consentimiento durante el periodo de Entrada - Salida utilizando este servicio
en modo Background con el objetivo de evidenciar su ubicación su ubicación de trabajo para
propósito exclusivamente laboral.
<br/>
<center> <b>Recolección automática de datos</b></center> <br/>

La app puede recopilar de forma automática cierto tipo de información, como ser: tipo de
dispositivo móvil, país del usuario, dirección IP, el sistema operativo del dispositivo móvil e
información de cómo se usa la app.
<br/>

<center>  <b>Política de retención y manejo de información</b>  </center><br/>
Conservaremos los datos brindados por el usuario y los recolectados automáticamente de forma
indefinida, ya que se necesita para el uso correcto de la app.
<br/>
 <center> <b>Seguridad</b>  </center> <br/>
Nos preocupamos por salvaguardar la confidencialidad de su información, por lo que contamos con
las medidas necesarias para lograr este fin. Por ejemplo, limitamos el acceso a esta información a
los empleados y contratistas autorizados que necesitan conocer dicha información para operar,
desarrollar o mejorar nuestra app. Tenga en cuenta que, aunque nos esforzamos por proveer
un nivel de seguridad razonable para manejar y procesar la información, no existe ningún sistema
que pueda prevenir todas las brechas de seguridad potenciales. 
<br/>
<center> <b>Cambios</b> </center>  <br/>

Esta Política de Privacidad puede ser actualizada de vez en cuando por cualquier razón. Le
notificaremos de cualquier cambio en la misma, mediante la publicación de la nueva política de
privacidad en esta página.
<br/>

<center> <b>Control de su información personal</b> </center>  <br/>
Esta compañía no venderá, cederá ni distribuirá la información personal que es recopilada sin su
consentimiento, salvo que sea requerido por un juez con un orden judicial.
Se reserva el derecho de cambiar los términos de la presente Política de Privacidad en cualquier
momento.
<center><b>Su consentimiento</b>  </center> <br/>

Al utilizar los Servicios, usted está consintiendo al procesamiento de los datos de usuario
proporcionados tal como se establece en esta Política de Privacidad
