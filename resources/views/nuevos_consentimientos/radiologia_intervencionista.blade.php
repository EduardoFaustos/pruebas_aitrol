                    Las radiografias se le realizan mediante la utilización de un equipo llamado Arco en C que utilizan radiaciones ionizantes
                    que traspasan el cuerpo para obtener imágenes sin producir dolor en él paciente, lo que permite mostrar el progreso de la cirugía en cualquier
                    punto del procedimiento, facilitando una mayor precisión durante la misma. Como consecuencia obtenemos la realización de mejores procedimientos, menos invasivos y más exactos, mejores
                    resultados y recuperaciones más rápidas para nuestros pacientes.

